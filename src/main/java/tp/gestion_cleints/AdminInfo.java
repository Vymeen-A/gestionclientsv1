package tp.gestion_cleints;

public class AdminInfo {
    private String raisonSociale;
    private String nomPrenom;
    private String adresse;
    private String ville;
    private String ice;
    private String rc;
    private String tp;
    private String identifiantTva;
    private String regimeTva;
    private String email;
    private String phone;
    private String logoPath;

    public AdminInfo() {
    }

    // Getters and Setters
    public String getRaisonSociale() {
        return raisonSociale;
    }

    public void setRaisonSociale(String raisonSociale) {
        this.raisonSociale = raisonSociale;
    }

    public String getNomPrenom() {
        return nomPrenom;
    }

    public void setNomPrenom(String nomPrenom) {
        this.nomPrenom = nomPrenom;
    }

    public String getAdresse() {
        return adresse;
    }

    public void setAdresse(String adresse) {
        this.adresse = adresse;
    }

    public String getVille() {
        return ville;
    }

    public void setVille(String ville) {
        this.ville = ville;
    }

    public String getIce() {
        return ice;
    }

    public void setIce(String ice) {
        this.ice = ice;
    }

    public String getRc() {
        return rc;
    }

    public void setRc(String rc) {
        this.rc = rc;
    }

    public String getTp() {
        return tp;
    }

    public void setTp(String tp) {
        this.tp = tp;
    }

    public String getIdentifiantTva() {
        return identifiantTva;
    }

    public void setIdentifiantTva(String identifiantTva) {
        this.identifiantTva = identifiantTva;
    }

    public String getRegimeTva() {
        return regimeTva;
    }

    public void setRegimeTva(String regimeTva) {
        this.regimeTva = regimeTva;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public String getPhone() {
        return phone;
    }

    public void setPhone(String phone) {
        this.phone = phone;
    }

    public String getLogoPath() {
        return logoPath;
    }

    public void setLogoPath(String logoPath) {
        this.logoPath = logoPath;
    }
}
